import { ICompletedExercise } from '../../exercise/exerciseInterface';

function generateCompletedExercises(
    exercise_name: string,
    username: string,
    rest: number,
    number_sets: number,
    repetitions: number,
    weight: number,
    greatest_weight: number,
    greatest_theorical_onerm: number,
    greatest_volume_oneset: number,
    greatest_volume: number,
): ICompletedExercise {
    const sets = [];
    for (let i = 0; i < number_sets; i++) {
        sets.push({ repetitions, weight });
    }
    return {
        exercise_name,
        username,
        rest,
        sets,
        greatest_weight,
        greatest_theorical_onerm,
        greatest_volume_oneset,
        greatest_volume,
    };
}
